def get_image_for_weather_id(weather_id):
    """
    Return the appropriate Minitel image bytes for a given OpenWeatherMap condition ID.

    OpenWeatherMap condition groups:
      2xx  -> Thunderstorm
      3xx  -> Drizzle  (shower rain)
      5xx  -> Rain
      6xx  -> Snow
      7xx  -> Atmosphere (fog, mist, haze…)
      800  -> Clear sky
      801  -> Few clouds  (11–25 %)
      802  -> Scattered clouds (25–50 %)
      803  -> Broken clouds (51–84 %)
      804  -> Overcast clouds (85–100 %) -> broken clouds image
    """
    if weather_id is None:
        return None
    if 200 <= weather_id < 300:
        return thunderstorm
    if 300 <= weather_id < 400:
        return shower_rain
    if 500 <= weather_id < 600:
        if weather_id in (500, 501):
            return rain
        return shower_rain
    if 600 <= weather_id < 700:
        return snow
    if 700 <= weather_id < 800:
        return atmosphere
    if weather_id == 800:
        return clear_sky
    if weather_id == 801:
        return few_clouds
    if weather_id == 802:
        return scattered_clouds
    if weather_id in (803, 804):
        return broken_clouds
    return None
